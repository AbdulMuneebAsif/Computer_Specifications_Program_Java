public class MotherBoard
{
//    private data member
    private String compName;
//    default constructor
    public MotherBoard()
    {
        compName = "intel";
    }
//    getter function
    public String getCompName()
    {
        return compName;
    }
//    setter function
    public void setCompName(String compName)
    {
        this.compName = compName;
    }

}